package javaexamples;

public class Arraystring {
	public static void main(String[] args)
	{
		String[] cities = {"hyd","Chennai","Bangalore"};
		for(String c:cities)
		{
			System.out.println(c);
		}
		for(int i=0;i<cities.length;i++)
		{
			System.out.println(cities[i]);
		}
	}
				

}
